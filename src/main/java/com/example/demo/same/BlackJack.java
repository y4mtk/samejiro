package com.example.demo.same;

public class BlackJack {

	private int card;
	private int pcard;

	public BlackJack() {

	}

	public BlackJack(int card,int pcard) {
		this.card = card;
		this.pcard = pcard;
	}

	public int getCard() {
		return card;
	}

	public void setCard(int card) {
		this.card = card;
	}

	public int getPcard() {
		return pcard;
	}

	public void setPcard(int pcard) {
		this.pcard = pcard;
	}


}
